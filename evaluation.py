def position_eval(square, piece):
    # Determine the position of the piece and get their value
    index = square if piece.color == chess.WHITE else (63 - square)
    table = psqt.get(piece.piece_type, None)
    return table[index]

# Evaluate the current board position and return who's winning
def evaluation(board, curr_player):
    # Initialize scores
    score = 0

    # Count the value of pieces on board and determine who is leading
    for square in chess.SQUARES:
        piece = board.piece_at(square)
        
        if piece:
            value = piece_values[piece.piece_type]
            position_score = position_eval(square, piece)

            if piece.color == curr_player.player:
                score += value + position_score
            else:
                score -= value + position_score

    return score